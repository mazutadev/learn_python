#!/usr/bin/env python3
# -*- coding: utf-8 -*-

"""
Простой пример использования функции print()
"""

# Простой вывод текста
print("Hello, World!")

# Вывод нескольких аргументов
print("Hello", "World", "!")

# Вывод с использованием разделителя
print("Hello", "World", "!", sep="-")

# Вывод с завершающим символом
print("Hello", end=" ")
print("World", end="!\n")

# Вывод с форматированием
name = "Python"
print(f"Hello, {name}!")
