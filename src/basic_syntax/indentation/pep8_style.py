# Отступы в многострочных конструкциях
def multiline_structures():
    """Функция демонстрирует отступы в многострочных конструкциях."""
    # Правильные отступы в списке
    my_list = [
        "первый элемент",
        "второй элемент",
        "третий элемент",
    ]

    # Правильные отступы в словаре
    my_dict = {
        "ключ1": "значение1",
        "ключ2": "значение2",
        "ключ3": "значение3",
    }

    # Правильные отступы в вызове функции
    result = some_function(
        arg1="значение1",
        arg2="значение2",
        arg3="значение3",
    )

    return result


# Отступы в условных выражениях
def conditional_indentation():
    """Функция демонстрирует правильные отступы в условных выражениях."""
    x = 10
    y = 20

    # Правильные отступы в if-elif-else
    if x > y:
        print("x больше y")
    elif x < y:
        print("x меньше y")
    else:
        print("x равно y")

    # Правильные отступы в тернарном операторе
    result = "x больше y" if x > y else "x меньше y" if x < y else "x равно y"

    return result
