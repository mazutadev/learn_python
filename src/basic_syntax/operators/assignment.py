#!/usr/bin/env python3
# -*- coding: utf-8 -*-

"""
Примеры операторов присваивания в Python
"""

# Простое присваивание
x = 10
print(f"x = {x}")

# Присваивание с арифметическими операциями
x += 5  # x = x + 5
print(f"x += 5: {x}")

x -= 3  # x = x - 3
print(f"x -= 3: {x}")

x *= 2  # x = x * 2
print(f"x *= 2: {x}")

x /= 4  # x = x / 4
print(f"x /= 4: {x}")

# Присваивание с целочисленным делением
x = 10
x //= 3  # x = x // 3
print(f"x //= 3: {x}")

# Присваивание с остатком от деления
x = 10
x %= 3  # x = x % 3
print(f"x %= 3: {x}")

# Присваивание с возведением в степень
x = 2
x **= 3  # x = x ** 3
print(f"x **= 3: {x}")

# Присваивание с побитовыми операциями
x = 0b1010  # 10
x &= 0b1100  # x = x & 0b1100
print(f"x &= 0b1100: {bin(x)}")

x = 0b1010
x |= 0b1100  # x = x | 0b1100
print(f"x |= 0b1100: {bin(x)}")

x = 0b1010
x ^= 0b1100  # x = x ^ 0b1100
print(f"x ^= 0b1100: {bin(x)}")

x = 0b1010
x <<= 2  # x = x << 2
print(f"x <<= 2: {bin(x)}")

x = 0b1010
x >>= 2  # x = x >> 2
print(f"x >>= 2: {bin(x)}")

# Множественное присваивание
a, b, c = 1, 2, 3
print(f"a = {a}, b = {b}, c = {c}")

# Обмен значений
a, b = b, a
print(f"После обмена: a = {a}, b = {b}")

# Присваивание с распаковкой
numbers = [1, 2, 3]
x, y, z = numbers
print(f"x = {x}, y = {y}, z = {z}")

# Присваивание с остатком
first, *rest = [1, 2, 3, 4, 5]
print(f"first = {first}, rest = {rest}")
